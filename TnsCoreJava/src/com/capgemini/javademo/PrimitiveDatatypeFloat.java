//This program of float primitive data type
package com.capgemini.javademo;

public class PrimitiveDatatypeFloat {

	public static void main(String[] args) {
		//create variables of different data type
		int valueFirst = 9/2;
		float valueSecond = 10f/6f;
		double valueThird = 10d/6d;
		System.out.println("value  1 = "+ valueFirst);
		System.out.println("value  2 = "+ valueSecond);
		System.out.println("value  3 = "+ valueThird);
	    
		int marker = 512;
	    double percentage = marker * 0.46f;
	    
	    System.out.println("percentage:"+percentage);

	}

}
